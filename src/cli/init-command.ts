// src/cli/init-command.ts
import fs from 'fs';
import path from 'path';
import os from 'os';
import * as readline from 'readline';
import { reloadEnv } from '../core/config.js';

export async function deepAgentsInitCommand() {
  // Always set up keys (overwrite existing .env.local)
  const envPath = path.join(os.homedir(), '.env.local');

  console.log(
    'Please provide your LLM API keys:\n' +
    '(You can set these later by editing .env.local)\n'
  );

  // Collect API keys
  const anthropicKey = await askQuestion(
    'ANTHROPIC_API_KEY (Claude - optional, press Enter to skip): '
  );

  const openaiKey = await askQuestion(
    'OPENAI_API_KEY (GPT - optional, press Enter to skip): '
  );

  const googleKey = await askQuestion(
    'GOOGLE_API_KEY (Gemini - optional, press Enter to skip): '
  );

  if (!anthropicKey && !openaiKey && !googleKey) {
    console.log('Error: At least one API key must be provided.');
    return;
  }

  const backendUrl = await askQuestion('Backend URL (http://localhost:4200): ') || 'http://localhost:4200';

  const dashboardPort = await askQuestion('Dashboard port (4201): ') || '4201';

  // Create .env.local
  const envContent = `# Nexus Configuration
# Generated by buildwithnexus init

# LLM API Keys
${anthropicKey ? `ANTHROPIC_API_KEY=${anthropicKey}` : '# ANTHROPIC_API_KEY='}
${openaiKey ? `OPENAI_API_KEY=${openaiKey}` : '# OPENAI_API_KEY='}
${googleKey ? `GOOGLE_API_KEY=${googleKey}` : '# GOOGLE_API_KEY='}

# Backend
BACKEND_URL=${backendUrl}
DASHBOARD_PORT=${dashboardPort}

# Optional
# LOG_LEVEL=debug
`;

  fs.writeFileSync(envPath, envContent, { mode: 0o600 });

  reloadEnv(envPath);
  console.log('Configuration saved to .env.local and loaded into environment.');
}

// Global state for interactive vs piped input handling
let inputLines: string[] = [];
let lineIndex = 0;
let isSetup = false;

function setupInputHandling(): Promise<void> {
  return new Promise((resolve) => {
    if (isSetup) {
      resolve();
      return;
    }

    isSetup = true;

    // Check if stdin is a TTY (interactive) or piped
    if (process.stdin.isTTY) {
      // Interactive mode - no need to pre-read lines
      resolve();
    } else {
      // Piped input mode - collect all lines first
      const rl = readline.createInterface({
        input: process.stdin,
        output: process.stdout,
        terminal: false,
      });

      rl.on('line', (line) => {
        inputLines.push(line);
      });

      rl.on('close', () => {
        resolve();
      });
    }
  });
}

async function askQuestion(prompt: string): Promise<string> {
  await setupInputHandling();

  if (process.stdin.isTTY) {
    // Interactive mode - use readline.question
    return new Promise((resolve) => {
      const rl = readline.createInterface({
        input: process.stdin,
        output: process.stdout,
      });

      rl.question(prompt, (answer) => {
        rl.close();
        resolve(answer);
      });
    });
  } else {
    // Piped input mode - use pre-collected lines
    process.stdout.write(prompt);
    const answer = inputLines[lineIndex] || '';
    lineIndex++;
    console.log(answer); // Echo the answer for visibility
    return answer;
  }
}
